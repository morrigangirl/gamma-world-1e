#!/usr/bin/env node
/**
 * Rulebook prose extractor.
 *
 * Reads `ref/gamma-world-core-rules.pdf` through the Poppler `pdftotext`
 * utility and writes per-chapter Markdown files into
 * `ref/rulebook-prose/` using the filename convention the importer
 * (`scripts/import-rulebook-prose.mjs`) already understands.
 *
 * Workflow:
 *   1. `npm run extract:rulebook-prose`   (this tool)
 *   2. Proofread `ref/rulebook-prose/NN-chapter.md` — OCR artifacts are
 *      inevitable; fix obvious misreads before importing.
 *   3. `npm run import:rulebook-prose && npm run build:compendia`
 *
 * How it works:
 *   - The PDF is a letter-sized (612 × 792 pt) two-column layout.
 *   - We call `pdftotext -layout` twice per page: once for the left
 *     half (x = 0, w = 306) and once for the right half (x = 306,
 *     w = 306). That yields text in reading order (left column first,
 *     then right column) which the default full-page extractor does
 *     not do cleanly.
 *   - We detect the top-of-page chapter heading (first ~10 non-empty
 *     lines of either column) by matching a curated list of known
 *     chapter titles. We allow the first letter of the title to be
 *     absent — the source PDF was generated by OCRmyPDF + Tesseract
 *     and frequently drops the drop-cap leading glyph on headings.
 *   - Within each chapter we promote ALL-CAPS one-line headers to H2
 *     ("## Section"). Numbered mutation/equipment entries like
 *     "4. DENSITY CONTROL:" become H3 ("### 4. Density Control").
 *   - Hyphenation line breaks (`some-\nthing`) are rejoined and
 *     paragraphs are unwrapped so the result reads as flowing prose.
 *
 * We do not try to correct OCR errors beyond obvious mechanical
 * artifacts. Words like "hirn" or "cultura!" stay — the output is a
 * first-pass transcription that the owner will polish by hand.
 */

import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const pdfPath = path.join(repoRoot, "ref", "gamma-world-core-rules.pdf");
const outDir = path.join(repoRoot, "ref", "rulebook-prose");

// Page geometry of this PDF. Confirmed via `pdfinfo`.
const PAGE_WIDTH = 612;
const PAGE_HEIGHT = 792;
const COLUMN_WIDTH = Math.floor(PAGE_WIDTH / 2);

/**
 * Chapter definitions, in document order. `header` is the exact heading
 * we expect to find at the top of the first page of the chapter; we
 * additionally accept the header with its first letter dropped to
 * tolerate common OCR drop-cap loss. `slug` matches the filename the
 * importer recognizes.
 *
 * "HOW TO USE THIS BOOK" content is folded into Chapter 2 (Designing
 * Gamma World) because the importer has no dedicated chapter for it.
 *
 * Chapter 10 ("EXAMPLE OF PLAY") is kept as a boundary marker only so
 * chapter 9 ("EXPERIENCE") knows where to stop. It is flagged `skip`
 * so no output file is written for it: the chapter opens on a full-page
 * map graphic whose OCR output is unusable garbage (hundreds of 2–3
 * character fragments of map labels) that also hangs the importer.
 * Owner decision was to exclude the example-of-play chapter from the
 * compendium rulebook entirely.
 */
const CHAPTERS = [
  { slug: "01-introduction",                  header: "INTRODUCTION" },
  { slug: "02-designing-gamma-world",         header: "DESIGNING GAMMA WORLD" },
  { slug: "03-creating-characters",           header: "CREATING CHARACTERS" },
  { slug: "04-mutations",                     header: "MUTATIONS" },
  { slug: "05-play-of-the-game",              header: "PLAY OF THE GAME" },
  { slug: "06-artifacts-and-equipment",       header: "ARTIFACTS AND EQUIPMENT" },
  { slug: "07-standard-devices-and-materials", header: "STANDARD DEVICES, UNITS, AND MATERIALS" },
  { slug: "08-robotic-units",                 header: "ROBOTIC UNITS" },
  { slug: "09-experience",                    header: "EXPERIENCE" },
  { slug: "10-example-of-play",               header: "EXAMPLE OF PLAY", skip: true }
];

const FIRST_CONTENT_PAGE = 4; // cover, credits, TOC occupy PDF pages 1–3

function runPdftotext(args) {
  return execFileSync("pdftotext", [...args, pdfPath, "-"], {
    encoding: "utf8",
    maxBuffer: 64 * 1024 * 1024
  });
}

function getPageCount() {
  const info = execFileSync("pdfinfo", [pdfPath], { encoding: "utf8" });
  const match = info.match(/Pages:\s+(\d+)/);
  if (!match) throw new Error("Could not parse page count from pdfinfo");
  return Number(match[1]);
}

/**
 * Extract a rectangular region of a single PDF page in column-preserving
 * layout mode. Coordinates are PDF points from top-left.
 */
function extractRegion(pdfPage, { x = 0, y = 0, w = PAGE_WIDTH, h = PAGE_HEIGHT } = {}) {
  return runPdftotext([
    "-layout",
    "-f", String(pdfPage),
    "-l", String(pdfPage),
    "-x", String(x),
    "-y", String(y),
    "-W", String(w),
    "-H", String(h)
  ]);
}

function extractPageColumns(pdfPage) {
  const left = extractRegion(pdfPage, { x: 0,            w: COLUMN_WIDTH });
  const right = extractRegion(pdfPage, { x: COLUMN_WIDTH, w: COLUMN_WIDTH });
  return { left, right };
}

function normalizeHeading(text) {
  return text
    .replace(/[^A-Za-z ]+/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .toUpperCase();
}

/**
 * Extract the leading ALL-CAPS heading cluster from a line.
 *
 * A cluster is a run of uppercase letters and spaces/commas terminated
 * by either a column-gap (≥4 consecutive whitespace characters) or the
 * end of the line. This distinguishes:
 *
 *   standalone heading:  `"        EXAMPLE OF PLAY        RA e"` → `EXAMPLE OF PLAY`
 *   body with prefix:    `"ROBOTIC UNITS is given in ..."`         → null
 *
 * In the body-text case the cluster is followed by a single space and
 * a lowercase letter, so neither terminator condition is satisfied and
 * the regex fails to find a valid cluster. In the heading case the
 * cluster is followed by either a wide column gap (pdftotext filler
 * between columns) or the end of the line, so the cluster is captured
 * cleanly.
 */
function extractHeadingCluster(line) {
  const match = line.match(/^\s*([A-Z][A-Z ,]*?)(?:\s{4,}|\s*$)/);
  if (!match) return null;
  const cluster = match[1].trim();
  return cluster || null;
}

/**
 * Match an extracted heading cluster against a known chapter header.
 * OCR from Tesseract sometimes drops the first character of a large
 * heading (the drop-cap or the first glyph of a styled all-caps run),
 * so we also accept the header with its first letter absent.
 */
function matchesChapterHeader(line, header) {
  const cluster = extractHeadingCluster(line);
  if (!cluster) return false;
  const L = normalizeHeading(cluster);
  const H = normalizeHeading(header);
  if (!L || !H) return false;
  if (L === H) return true;
  const Htrim = H.slice(1);                 // drop-cap-loss fallback
  if (Htrim && L === Htrim) return true;
  return false;
}

/**
 * Scan every non-empty line of both columns of a page looking for a
 * chapter header. Returns the matched chapter index or `-1` if none.
 * Chapter headers can appear mid-page when a chapter boundary is not
 * aligned to a page break (e.g. DESIGNING GAMMA WORLD begins on the
 * second half of page 6).
 */
function detectChapterStart(pageText) {
  const lines = pageText.split(/\r?\n/).map((l) => l.trim()).filter(Boolean);
  for (let i = 0; i < CHAPTERS.length; i += 1) {
    for (const line of lines) {
      if (matchesChapterHeader(line, CHAPTERS[i].header)) return i;
    }
  }
  return -1;
}

/**
 * Build a map from chapter index → first PDF page number. The first
 * page whose top lines match a chapter header wins. Later pages that
 * also match are ignored.
 */
function buildChapterPageMap(pageCount) {
  const pageMap = new Array(CHAPTERS.length).fill(null);
  for (let p = FIRST_CONTENT_PAGE; p <= pageCount; p += 1) {
    const { left, right } = extractPageColumns(p);
    const fromLeft  = detectChapterStart(left);
    const fromRight = detectChapterStart(right);
    const candidates = [fromLeft, fromRight].filter((i) => i >= 0);
    for (const idx of candidates) {
      if (pageMap[idx] == null) pageMap[idx] = p;
    }
  }
  return pageMap;
}

/**
 * Merge the two column strings of a page into a single logical flow.
 */
function mergeColumns({ left, right }) {
  return `${left}\n${right}`;
}

/**
 * Convert a block of raw PDF-extracted lines into a flat sequence of
 * typed blocks: `{ type: "heading", text }` or `{ type: "paragraph", text }`.
 *
 * We use two signals the PDF gives us:
 *
 *   - Blank lines mark hard paragraph breaks.
 *   - Within a run of non-blank lines, a line whose raw form begins
 *     with ≥2 leading spaces marks a new paragraph. (The source PDF
 *     indents the first line of every paragraph while continuation
 *     lines are flush-left.)
 *
 * Hyphenated line-break joins (`some-\nthing` → `something`) are
 * applied before paragraphs are emitted. Lines that are purely
 * non-letter garbage (dot leaders, stray punctuation) are dropped.
 */
function linesToBlocks(raw) {
  const lines = raw.split(/\r?\n/);
  const blocks = [];
  let current = [];

  const flush = () => {
    if (!current.length) return;
    let text = current.join(" ").replace(/\s+/g, " ").trim();
    text = text.replace(/([A-Za-z])-\s+([a-z])/g, "$1$2");
    if (text) blocks.push({ type: "paragraph", text });
    current = [];
  };

  for (const rawLine of lines) {
    if (!rawLine.trim()) {
      flush();
      continue;
    }
    const trimmed = rawLine.trim();
    if (!/[A-Za-z]/.test(trimmed) || trimmed.length <= 1) continue;

    // Heading cluster at the head of the line: if the rest of the line
    // (after the cluster) is empty or only non-letter noise (e.g.
    // right-column text bleed), treat this as a heading block.
    const cluster = extractHeadingCluster(rawLine);
    if (cluster && cluster.length >= 3) {
      const afterClusterMatch = rawLine.match(/^\s*[A-Z][A-Z ,]*?(\s{4,}.*|\s*)$/);
      const tail = afterClusterMatch ? (afterClusterMatch[1] ?? "") : "";
      const tailHasBody = /[a-z]/.test(tail) || /[A-Z]{3,}/.test(tail.replace(/[^A-Za-z]/g, ""));
      if (!tailHasBody) {
        flush();
        blocks.push({ type: "heading", text: cluster });
        continue;
      }
    }

    // Indent-based paragraph break within a run of non-blank lines.
    const leadingSpaces = rawLine.match(/^ */)[0].length;
    if (current.length && leadingSpaces >= 2) {
      flush();
    }
    current.push(trimmed);
  }
  flush();
  return blocks;
}

/**
 * Detect numbered entries like "4. DENSITY CONTROL: The mutant...",
 * "1. ATTRACTION ODOR (D): The mutant's body ...", or
 * "13. HEIGHTENED DEXTERITY. Mutants with ...".
 *
 * The source PDF was OCR'd and separators are inconsistent: the number
 * terminator may be `.` or `,` (digit-comma confusion), and the title
 * terminator may be `:` or `.`. We accept either. Titles may contain
 * parentheses for defect markers like "(D)".
 */
function detectNumberedHeader(text) {
  const match = text.match(/^(\d+)[.,]\s+([A-Z][A-Z0-9 ,'\/\-()]+?)[:.]\s+(.*)$/);
  if (!match) return null;
  const [, number, title, rest] = match;
  return { number, title: titleCase(title), rest: rest.trim() };
}

function titleCase(text) {
  return text
    .toLowerCase()
    .split(/\s+/)
    .map((word) => (word ? word[0].toUpperCase() + word.slice(1) : word))
    .join(" ")
    .replace(/\s+/g, " ")
    .trim();
}

/**
 * Title-case an ALL-CAPS heading while preserving known acronyms
 * (two-to-three letter all-caps words stay upper-case) and hyphenated
 * compounds.
 */
function headingTitleCase(upper) {
  const minorWords = new Set(["of", "the", "and", "or", "for", "in", "on", "to", "a", "an"]);
  const parts = upper.toLowerCase().split(/(\s+|[-\/])/);
  return parts
    .map((part, idx) => {
      if (!part || /^\s+$/.test(part) || part === "-" || part === "/") return part;
      if (idx > 0 && minorWords.has(part)) return part;
      return part[0].toUpperCase() + part.slice(1);
    })
    .join("");
}

/**
 * Convert typed blocks into Markdown, inserting `## Section` breaks
 * where heading clusters appear and `### n. Name` breaks where
 * numbered entries (mutation list items, artifact entries) appear.
 */
function blocksToMarkdown(blocks, chapterHeader) {
  const out = [];
  let firstSectionSeen = false;

  const ensureSection = () => {
    if (firstSectionSeen) return;
    out.push("## Overview");
    out.push("");
    firstSectionSeen = true;
  };

  for (const block of blocks) {
    if (block.type === "heading") {
      // Skip the chapter title itself; it's inferred from the filename.
      if (matchesChapterHeader(block.text, chapterHeader)) continue;
      out.push("");
      out.push(`## ${headingTitleCase(block.text)}`);
      out.push("");
      firstSectionSeen = true;
      continue;
    }

    // Paragraph block. Check for a numbered sub-entry pattern before
    // emitting as body text.
    const numbered = detectNumberedHeader(block.text);
    if (numbered) {
      ensureSection();
      out.push("");
      out.push(`### ${numbered.number}. ${numbered.title}`);
      if (numbered.rest) {
        out.push("");
        out.push(numbered.rest);
      }
      out.push("");
      continue;
    }

    ensureSection();
    out.push(block.text);
    out.push("");
  }

  // Collapse multiple consecutive blank lines.
  const compact = [];
  let prevBlank = false;
  for (const line of out) {
    const isBlank = line.trim() === "";
    if (isBlank && prevBlank) continue;
    compact.push(line);
    prevBlank = isBlank;
  }
  return compact.join("\n").trim() + "\n";
}

function extractChapterText(startPage, endPage) {
  const parts = [];
  for (let p = startPage; p <= endPage; p += 1) {
    const cols = extractPageColumns(p);
    parts.push(mergeColumns(cols));
    // Use an empty line between columns/pages so paragraph splitting
    // survives across page boundaries.
    parts.push("");
  }
  return parts.join("\n");
}

function ensureOutDir() {
  if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });
}

function shouldOverwrite(filePath) {
  // Never silently overwrite hand-edited content. If a file exists and
  // was not produced by this extractor, bail out for that slug.
  if (!fs.existsSync(filePath)) return true;
  const head = fs.readFileSync(filePath, "utf8").slice(0, 400);
  return /<!-- GENERATED BY extract-rulebook-prose\.mjs -->/.test(head);
}

function writeChapter(slug, chapterHeader, markdown) {
  const filePath = path.join(outDir, `${slug}.md`);
  if (!shouldOverwrite(filePath)) {
    console.log(`  skipping ${slug}: file exists and was hand-edited`);
    return;
  }
  const header = [
    "<!-- GENERATED BY extract-rulebook-prose.mjs -->",
    "<!-- Auto-OCR'd from ref/gamma-world-core-rules.pdf. Proofread before importing. -->",
    "",
    markdown
  ].join("\n");
  fs.writeFileSync(filePath, header, "utf8");
  const bytes = Buffer.byteLength(header, "utf8");
  console.log(`  wrote ${slug}.md (${bytes} bytes)`);
}

function main() {
  if (!fs.existsSync(pdfPath)) {
    console.error(`No rulebook PDF at ${pdfPath}. Place the source PDF there and retry.`);
    process.exitCode = 1;
    return;
  }

  ensureOutDir();
  const totalPages = getPageCount();
  console.log(`Rulebook PDF: ${path.basename(pdfPath)} (${totalPages} pages)`);
  console.log(`Scanning for chapter boundaries…`);

  const startPages = buildChapterPageMap(totalPages);
  for (let i = 0; i < CHAPTERS.length; i += 1) {
    if (startPages[i] == null) {
      console.warn(`  !! could not locate chapter ${CHAPTERS[i].slug} (${CHAPTERS[i].header})`);
    }
  }

  // Derive each chapter's end page from the next located chapter's start.
  const endPages = new Array(CHAPTERS.length).fill(null);
  const located = startPages
    .map((p, i) => ({ p, i }))
    .filter(({ p }) => p != null)
    .sort((a, b) => a.p - b.p);

  for (let k = 0; k < located.length; k += 1) {
    const { i, p } = located[k];
    const nextStart = located[k + 1]?.p ?? (totalPages + 1);
    // Cap at page 45: beyond that is appendix/reference sheets, not chapter prose.
    endPages[i] = Math.min(nextStart - 1, 45);
  }

  console.log("");
  console.log("Chapter page ranges (PDF pages):");
  for (let i = 0; i < CHAPTERS.length; i += 1) {
    const s = startPages[i];
    const e = endPages[i];
    if (s == null) continue;
    console.log(`  ${CHAPTERS[i].slug.padEnd(40)} pages ${s}–${e}`);
  }
  console.log("");

  for (let i = 0; i < CHAPTERS.length; i += 1) {
    const { slug, header, skip } = CHAPTERS[i];
    const start = startPages[i];
    const end = endPages[i];
    if (start == null || end == null) continue;
    if (skip) {
      console.log(`  skipping ${slug} (boundary-only chapter; not written)`);
      continue;
    }
    const raw = extractChapterText(start, end);
    const blocks = linesToBlocks(raw);
    const markdown = blocksToMarkdown(blocks, header);
    writeChapter(slug, header, markdown);
  }

  console.log("");
  console.log(`Done. Output in ${path.relative(repoRoot, outDir)}/`);
  console.log("Next: proofread the files, then run:");
  console.log("  npm run import:rulebook-prose && npm run build:compendia");
}

main();
